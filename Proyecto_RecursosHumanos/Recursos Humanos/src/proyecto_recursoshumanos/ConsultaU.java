     public void cargarComboboxfiltro(int filtro)
    {
          DefaultComboBoxModel combo1 = new DefaultComboBoxModel();
          if(filtro==1)
          {
              combo1.addElement("Acceso");
              combo1.addElement("Estado");
          }
           if(filtro==2)
          {
              combo1.addElement("Estado");
          }
          
              

          ComboFiltro.setModel(combo1);
          ComboFiltro.setSelectedIndex(-1);
          combofiltrox.setSelectedIndex(-1);
    }
    public void cargarComboboxfiltrox(String filtro){
          if("Acceso".equals(filtro))
          {
              combofiltrox.setModel(con.cargarComboAcceso());
          }
           if("Estado".equals(filtro))
          {
              combofiltrox.setModel(con.cargarComboEstado());
          }

    }
